    private void addJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addJButtonActionPerformed

        Organization org = (Organization) cbOrgList.getSelectedItem();
        String name = nameJTextField.getText();
        if(!name.matches("[A-Za-z]+"))
        {
        JOptionPane.showMessageDialog(null,"Employee name is invalid","WARNING",JOptionPane.WARNING_MESSAGE);
        return;
        }
        org.getStaffDirectory().createEmployee(name);
        JOptionPane.showMessageDialog(null,"Employee created successfully");
        Organization organization = (Organization) cbOrganisationEmp.getSelectedItem();
        if (organization != null){
            populateEmployeesTable(organization);
        }
        
    }//GEN-LAST:event_addJButtonActionPerformed

