    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) drugTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest wr : enterprise.getWorkQueue().getLstWorkRequest()) {
            Object row[] = new Object[4];
            row[0] = wr;
            row[1] = wr.getStatus();
            row[2] = wr.getPerson();
            row[3] = wr.getReceiver();
            dtm.addRow(row);
        }

    }

