    private void btnProceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProceedActionPerformed
        // TODO add your handling code here:
        // String email = txtEmailId.getText();
      //  String phoneNo = txtPhoneNo.getText();
       String street=txtStreet.getText();
       String city=txtCity.getText();
       String state=txtState.getText();
       String ssn=txtSSn.getText();
       String zip=txtZip.getText();
        //TODO VALIDATION
        
        
        String name=txtName.getText();
        if(!ssn.matches("[0-9]{6}"))
                    {
                       JOptionPane.showMessageDialog(null, "Please enter a valid 10 digit phone number ","WARNING",JOptionPane.WARNING_MESSAGE);
                        return;
                    }
                    if(!street.matches("[A-Za-z 0-9]+"))
                    {
                     JOptionPane.showMessageDialog(null, "Please enter a valid street name ","WARNING",JOptionPane.WARNING_MESSAGE);
                        return;   
                    }
                    if(!city.matches("[A-Za-z]+"))
                    {
                     JOptionPane.showMessageDialog(null, "Please enter a valid city name ","WARNING",JOptionPane.WARNING_MESSAGE);
                        return;   
                    }
                    if(!state.matches("[A-Za-z]+"))
                    {
                     JOptionPane.showMessageDialog(null, "Please enter a valid state name ","WARNING",JOptionPane.WARNING_MESSAGE);
                        return;   
                    }
                    if(!zip.matches("[0-9]{5}"))
                    {
                     JOptionPane.showMessageDialog(null, "Please enter a valid country name ","WARNING",JOptionPane.WARNING_MESSAGE);
                        return;   
                    }
        
        if(!name.matches("[A-Za-z]+"))
        {
            JOptionPane.showMessageDialog(null, "Please enter a valid name","WARNING",JOptionPane.WARNING_MESSAGE);
            return;
        }

        if(!rblMaleFm.isSelected() && !rblFemaleFM.isSelected())
        {
            JOptionPane.showMessageDialog(null, "Please select your gender","WARNING",JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        FamilyMember fm=organization.addFamilyMember();
        if(rblMaleFm.isSelected())
        {
            fm.setGender(Gender.Male);
        }
        else
        {
            fm.setGender(Gender.Female);
        }
        fm.setFmName(name);
        fm.setPatient(wr.getPerson());
        fm.setCity(city);
        fm.setState(state);
        fm.setStreet(street);
        fm.setNoOffm((int) spinnerNoF.getValue());
        //fm.setssn(Integer.toString(ssn));
      //  String s=(String) cbRelationship.getSelectedItem();
       // fm.setRlp((FamilyMember.Relationship) cbRelationship.getSelectedItem());
       
        //fm.setNoOffm((int) spinnerNoF.getValue());
       userProcessContainer.add(new BackgroundCheckJPanel(userProcessContainer, wr,account, organization, etrp, business));
                ((CardLayout)userProcessContainer.getLayout()).next(userProcessContainer);

    }//GEN-LAST:event_btnProceedActionPerformed


