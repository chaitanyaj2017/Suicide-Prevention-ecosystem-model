   public void populatePersonDetails()
   {
       DefaultTableModel dtm=(DefaultTableModel) tblPatients.getModel();
      
       for(Person p:organisation.getPersonDirectory().getLstPerson())
       {
         Object[] row=new Object[4];
         row[0]=p;
         row[1]=p.getAge();
         row[2]=p.getGender().getValue();
         row[3]=p.getPreliminaryCheck();
         
         dtm.addRow(row);
       }
   }
