    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        String name = networkT.getText();
        if (!name.matches("[A-Za-z]+")) {
            JOptionPane.showMessageDialog(null, "Please enter a valid Network Name", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        business.getNetworkDir().addNetwork(name);
        populateNetworkTable();
        JOptionPane.showMessageDialog(null, "Network Added Successfully!!");
        networkT.setText("");
    }//GEN-LAST:event_createBtnActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        SystemAdminWorkAreaJPanel sysAdminwjp = (SystemAdminWorkAreaJPanel) component;
        sysAdminwjp.populateTree();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_backBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int selectedrow = networkTable.getSelectedRow();
        if (selectedrow < 0) {
            JOptionPane.showMessageDialog(null, "Nothing selected to delete");
            return;
        }
        Network n = (Network) networkTable.getValueAt(selectedrow, 0);
        business.getNetworkDir().removeNetwork(n);
        JOptionPane.showMessageDialog(null, "Network Deleted Successfully!!");
        populateNetworkTable();

    }//GEN-LAST:event_deleteBtnActionPerformed


