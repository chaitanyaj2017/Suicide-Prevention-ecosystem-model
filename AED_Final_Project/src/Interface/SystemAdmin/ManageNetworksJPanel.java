    public void populateNetworkTable() {
        DefaultTableModel dtm = (DefaultTableModel) networkTable.getModel();

        dtm.setRowCount(0);
        for (Network n : business.getNetworkDir().getLstNetwork()) {
            Object[] row = new Object[1];
            row[0] = n;
            dtm.addRow(row);
        }
    }

