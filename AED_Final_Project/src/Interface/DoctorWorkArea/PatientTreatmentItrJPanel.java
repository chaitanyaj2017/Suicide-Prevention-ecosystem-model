    public void populatePatientDetails()
    {
     txtPersonName.setText(wr.getPerson().getPersonName());
     txtEmailId.setText(wr.getPerson().getEmail());
     txtContactNo.setText(Long.toString(wr.getPerson().getPhoneNo()));
     txtDob.setText(wr.getPerson().getDateOfBirth().toString());
     //
     JFileChooser jc = new JFileChooser();
        jc.showOpenDialog(null);
        File f = jc.getSelectedFile();
        //String fn = f.getAbsolutePath()
        //picAddress.setText(fn);
        String path=wr.getPerson().getProfilePhoto();
        ImageIcon i = new ImageIcon(path);
        //labelname.setIcon(icon);
        profilePic.setIcon(i);
     if(wr.getPerson().getGender().equals(Person.Gender.Male))
     {
         rblMaleGender.setSelected(true);
     }
     else
     {
         rblFemaleGender.setSelected(true);
     }
//        SimpleDateFormat sf=new SimpleDateFormat("MM-dd-yyyy");
//     Date d=wr.getPerson().getDateOfBirth();
//     txtDob.setText(sf.format(d));
     he=(HospitalEnterprise) etrp;
     //txtEmailId.setText(wr.getPerson());
     for(MedicalRecord mrd:he.getMedicalRecordDir().getLstMedicalRecord())
     {
         if(mrd.getPerson()==wr.getPerson())
         {
             mr=mrd;
             break;
         }
     }
     if(mr!=null)
     {
         txtBloodPressure.setText(mr.getVitalSign().getHighbloodPressure()+" - "+mr.getVitalSign().getLowBloodPressure());
         txtHeartRate.setText(Integer.toString(mr.getVitalSign().getHeartRate()));
         txtDrugTest.setText(Float.toString(mr.getDrugTestResults()));
         txtAlcoholResults.setText(Float.toString(mr.getAlcoholResults()));
         
     }
    }
