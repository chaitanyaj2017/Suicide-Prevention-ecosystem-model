    private void populateSuspect()
    {
         DefaultTableModel dtm=(DefaultTableModel) SuspectTbl.getModel();
       for(Person p:organisation.getPersonDirectory().getLstPerson())
       {
         Object[] row=new Object[4];
         row[0]=p;
         row[1]=p.getAge();
         row[2]=p.getGender().getValue();
         row[3]=p.getSsnNumber();
         dtm.addRow(row);
       }
  
    
    
    }
    
