    private void removeBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeBtnActionPerformed
      int selectedRow=  SuspectTbl
              .getSelectedRow();
        if(selectedRow>-1)
        {
            Person p=(Person) SuspectTbl.getValueAt(selectedRow,0);
            int op=JOptionPane.showConfirmDialog(null,"Are you sure you want to remove this Suspect?");
            if(op==0)
            {
            organisation.getPersonDirectory().removePerson(p);
            JOptionPane.showMessageDialog(null,"Suspect Removed successfully");
            }
            else
                return;
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Please select a suspect from table","WARNING",JOptionPane.WARNING_MESSAGE);
        }
        
    }//GEN-LAST:event_removeBtnActionPerformed

