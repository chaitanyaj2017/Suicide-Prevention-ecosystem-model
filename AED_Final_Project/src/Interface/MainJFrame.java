    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String userName = txtUserId.getText();
        // Get Password
        char[] passwordCharArray = txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);
       // boolean flag = false;

        UserAccount userAccount = null;
        Enterprise inEnterprise=null;
        Organization inOrganization=null;
        //todo fetch user account
        userAccount=business.getUserAccountDirectory().authenticateUser(userName, password);
        if(userAccount==null)
        {
            //         JOptionPane.showMessageDialog(null,"UserId/Password is incorrect");
            for(Network n:business.getNetworkDir().getLstNetwork())
            {
                for(Enterprise e:n.getEnterpriseDir().getLstEnterprises())
                {
                    userAccount=e.getUserAccountDirectory().authenticateUser(userName, password);
                    if(userAccount==null)
                    {
                        for(Organization o:e.getOrdDir().getLstOrganisation())
                        {
                            userAccount=o.getUserAccountDirectory().authenticateUser(userName, password);
                            if(userAccount!=null)
                            {
                                inEnterprise=e;
                                inOrganization=o;
                                break;
                            }
                        }
                    }
                    else
                    {
                        inEnterprise=e;
                        break;
                    }
                    if(inOrganization!=null)
                    {
                        break;
                    }
                }
                if(inEnterprise!=null)
                {
                    break;
                }
            }

        }
        if(userAccount!=null)
        {
            UserProcessContainer.add("WorkArea",userAccount.getRole().createWorkArea(UserProcessContainer, userAccount, inOrganization, inEnterprise, business));
            ((CardLayout)UserProcessContainer.getLayout()).next(UserProcessContainer);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please enter valid credentials","WARNING",JOptionPane.WARNING_MESSAGE);
            txtUserId.setText("");
            txtPassword.setText("");
            return;
        }

        btnLogin.setEnabled(false);
        btnLogout.setEnabled(true);
        txtUserId.setEnabled(false);
        txtUserId.setText("");
        txtPassword.setText("");
        txtPassword.setEnabled(false);
    }//GEN-LAST:event_btnLoginActionPerformed

