package Business.DrugRecord;

/**
 *
 * @author Suyog Sathe
 */
public class Signs {
    private int metabolicRate;
    private int urinePh;
    private int bodyMass;
    private int drugTolerance;
    private int Cocaine;
    private int Weed;
    private int Marijuana;

    public int getMetabolicRate() {
        return metabolicRate;
    }

    public void setMetabolicRate(int metabolicRate) {
        this.metabolicRate = metabolicRate;
    }

    public int getUrinePh() {
        return urinePh;
    }

    public void setUrinePh(int urinePh) {
        this.urinePh = urinePh;
    }

    public int getBodyMass() {
        return bodyMass;
    }

    public void setBodyMass(int bodyMass) {
        this.bodyMass = bodyMass;
    }

    public int getDrugTolerance() {
        return drugTolerance;
    }

    public void setDrugTolerance(int drugTolerance) {
        this.drugTolerance = drugTolerance;
    }

    public int getCocaine() {
        return Cocaine;
    }

    public void setCocaine(int Cocaine) {
        this.Cocaine = Cocaine;
    }

    public int getWeed() {
        return Weed;
    }

    public void setWeed(int Weed) {
        this.Weed = Weed;
    }

    public int getMarijuana() {
        return Marijuana;
    }

    public void setMarijuana(int Marijuana) {
        this.Marijuana = Marijuana;
    }

   
    
}
